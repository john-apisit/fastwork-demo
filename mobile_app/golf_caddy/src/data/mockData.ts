export function getTopCaddies(): Caddy[] {
  return caddies
    .filter((c) => c.isTopTen)
    .sort((a, b) => (a.popularityRank ?? 99) - (b.popularityRank ?? 99))
}

export function getCourseById(id: string) {
  return golfCourses.find((c) => c.id === id)
}

export function getCaddyById(id: string) {
  return caddies.find((c) => c.id === id)
}
